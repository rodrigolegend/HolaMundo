public class Calculadora {

    public static int sumar(int a, int b) {
        return a + b;
    }

    public static int restar(int a, int b) {
        return a - b;
    }

    public static int multiplicar(int a, int b) {
        return a *  b;
    }

    public static boolean esPar(int n) {
        return n % 2 == 0;
    }
}
